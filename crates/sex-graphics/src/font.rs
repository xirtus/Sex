/// Draw one character at (x, y). Transparent background when `bg` is None.
pub fn draw_char(buf: &mut WindowBuffer, x: u32, y: u32, c: u8, fg: u32, bg: Option<u32>) {
    let idx = (c & 0x7F) as usize;
    let glyph = &FONT[idx];
    for row in 0..8u32 {
        let byte = glyph[row as usize];
        for col in 0..8u32 {
            let set = (byte >> (7 - col)) & 1 != 0;
            if set {
                unsafe { buf.draw_pixel(x + col, y + row, fg); }
            } else if let Some(b) = bg {
                unsafe { buf.draw_pixel(x + col, y + row, b); }
            }
        }
    }
}

/// Draw a byte-string at (x, y). Returns the pixel width consumed.
pub fn draw_str(buf: &mut WindowBuffer, x: u32, y: u32, s: &[u8], fg: u32, bg: Option<u32>) -> u32 {
    let mut cx = x;
    for &c in s {
        draw_char(buf, cx, y, c, fg, bg);
        cx += CHAR_W;
    }
    cx.saturating_sub(x)
}

/// Pixel width of a string (characters × CHAR_W).
pub const fn str_width(len: usize) -> u32 {
    len as u32 * CHAR_W
}
